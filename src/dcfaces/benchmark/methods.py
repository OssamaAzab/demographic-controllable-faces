"""Per-method image generators for the benchmark.

Every method exposes the same small interface so the harness stays
method-agnostic:

    m = build_method(method_cfg, model_cfg, inference_cfg, device)
    phrase = m.identity_phrase(gender)          # fills {identity} in the prompt
    img = m.generate(id_name, gender, ref_path, prompt, seed)
    m.close()

The reference-image methods (PuLID, PhotoMaker, IP-Adapter, HyperLoRA) live in
gitignored clones under external/; their heavy objects are built lazily in each
__init__ and per-identity face embeddings / LoRAs are cached. HyperLoRA is the
one method that runs on RealVisXL rather than SDXL 1.0 (it is base-model
incompatible with vanilla SDXL -- documented exception), and consumes the ID
LoRAs pre-generated by scripts/08b_gen_hyperlora_loras.py.
"""

from __future__ import annotations

import gc
import os
import sys

import numpy as np
import torch

from dcfaces.paths import (
    DEMO_LORA_CKPTS,
    DREAMBOOTH_LORAS,
    INSIGHTFACE_DIR,
    MODELS_DIR,
    PROJECT_ROOT,
)

DEMO_LORA_DIR = DEMO_LORA_CKPTS / "step_4000"
EXTERNAL = PROJECT_ROOT / "external"
HYPERLORA_LORAS = MODELS_DIR / "hyperlora_loras"


def _fp16fix_vae(dtype=torch.float16):
    from diffusers import AutoencoderKL

    return AutoencoderKL.from_pretrained("madebyollin/sdxl-vae-fp16-fix", torch_dtype=dtype)


def _read_rgb(path) -> np.ndarray:
    from PIL import Image

    return np.array(Image.open(path).convert("RGB"))


def _add_path(p) -> None:
    p = str(p)
    if p not in sys.path:
        sys.path.insert(0, p)


class Method:
    """Base interface. Subclasses generate one image per (identity, prompt, seed)."""

    def identity_phrase(self, gender: str) -> str:
        raise NotImplementedError

    def generate(self, id_name: str, gender: str, ref_path, prompt: str, seed: int):
        raise NotImplementedError

    def close(self) -> None:
        pass


class DreamBoothMethod(Method):
    """A per-identity DreamBooth-LoRA, optionally stacked with the demographic LoRA."""

    def __init__(self, mcfg: dict, model_cfg: dict, infer_cfg: dict, device: str):
        from diffusers import StableDiffusionXLPipeline

        self.device = device
        self.use_demo = bool(mcfg.get("use_demo_lora", False))
        self.id_scale = float(mcfg.get("id_scale", 0.5))
        self.demo_scale = float(mcfg.get("demo_scale", 0.3))
        self.negative = infer_cfg["negative_prompt"]
        self.steps = int(infer_cfg["steps"])
        self.guidance = float(infer_cfg["guidance_scale"])
        self.resolution = int(infer_cfg["resolution"])
        self.pipe = StableDiffusionXLPipeline.from_pretrained(
            model_cfg["base"], vae=_fp16fix_vae(), torch_dtype=torch.float16, use_safetensors=True
        ).to(device)
        self.pipe.set_progress_bar_config(disable=True)
        self._loaded: str | None = None

    def identity_phrase(self, gender: str) -> str:
        return f"sks {gender}"

    def _ensure_identity(self, id_name: str) -> None:
        if self._loaded == id_name:
            return
        self.pipe.unload_lora_weights()
        self.pipe.load_lora_weights(str(DREAMBOOTH_LORAS / id_name), adapter_name="identity")
        if self.use_demo:
            self.pipe.load_lora_weights(str(DEMO_LORA_DIR), adapter_name="demo")
            self.pipe.set_adapters(["identity", "demo"], adapter_weights=[self.id_scale, self.demo_scale])
        else:
            self.pipe.set_adapters(["identity"], adapter_weights=[self.id_scale])
        self._loaded = id_name

    def generate(self, id_name, gender, ref_path, prompt, seed):
        self._ensure_identity(id_name)
        g = torch.Generator(device=self.device).manual_seed(seed)
        return self.pipe(
            prompt=prompt, negative_prompt=self.negative, num_inference_steps=self.steps,
            guidance_scale=self.guidance, height=self.resolution, width=self.resolution, generator=g,
        ).images[0]

    def close(self):
        del self.pipe
        self._loaded = None
        gc.collect()
        torch.cuda.empty_cache()


_PULID_DELAY_PATCHED = False


def _install_pulid_delay_patch():
    """Patch PuLIDPipeline.__call__ for delayed identity injection: zero the identity
    in the positive path for the first `_delay_start` diffusion steps, so the prompt
    establishes demographics before identity refines them. No-op when _delay_start is
    0/unset, so it is safe to leave installed for plain PuLID too."""
    global _PULID_DELAY_PATCHED
    if _PULID_DELAY_PATCHED:
        return
    from pulid.pipeline_v1_1 import PuLIDPipeline

    _orig_call = PuLIDPipeline.__call__

    def _delayed_call(self, x, sigma, **ea):
        i = getattr(self, "_delay_si", 0)
        self._delay_si = i + 1
        if i < getattr(self, "_delay_start", 0):
            pos = ea["positive"]["cross_attention_kwargs"]
            saved = pos["id_embedding"]
            pos["id_embedding"] = self._delay_uncond
            try:
                return _orig_call(self, x, sigma, **ea)
            finally:
                pos["id_embedding"] = saved
        return _orig_call(self, x, sigma, **ea)

    PuLIDPipeline.__call__ = _delayed_call
    _PULID_DELAY_PATCHED = True


class PuLIDMethod(Method):
    """PuLID v1.1 zero-shot identity, optionally stacked with the demographic LoRA.

    PuLID hardcodes relative model paths, so the pipeline is built with the CWD
    temporarily switched to external/PuLID; generation needs no relative paths.
    `delay_start` > 0 enables delayed identity injection (the demographic-control
    rescue): identity is held off for that many of the `steps` so the prompt sets
    age/race first.
    """

    def __init__(self, mcfg: dict, model_cfg: dict, infer_cfg: dict, device: str):
        self.device = device
        self.use_demo = bool(mcfg.get("use_demo_lora", False))
        self.id_scale = float(mcfg.get("id_scale", 0.8))
        self.demo_scale = float(mcfg.get("demo_scale", 0.7))
        self.delay_start = int(mcfg.get("delay_start", 0))
        self.negative = infer_cfg["negative_prompt"]
        self.steps = int(infer_cfg["steps"])
        self.guidance = float(infer_cfg["guidance_scale"])
        self.resolution = int(infer_cfg["resolution"])

        pulid_dir = EXTERNAL / "PuLID"
        _add_path(pulid_dir)
        cwd = os.getcwd()
        try:
            os.chdir(pulid_dir)
            from pulid.pipeline_v1_1 import PuLIDPipeline
            from pulid.utils import resize_numpy_image_long

            self._resize = resize_numpy_image_long
            self.pulid = PuLIDPipeline(sdxl_repo=model_cfg["base"], sampler="dpmpp_2m")
        finally:
            os.chdir(cwd)

        if self.delay_start > 0:
            _install_pulid_delay_patch()

        old_vae = self.pulid.pipe.vae
        self.pulid.pipe.vae = _fp16fix_vae().to(device)
        del old_vae
        self.pulid.pipe.vae.enable_tiling()   # PuLID calls vae.decode directly, so tile at the VAE
        self.pulid.pipe.vae.enable_slicing()
        if self.use_demo:
            self.pulid.pipe.load_lora_weights(str(DEMO_LORA_DIR), adapter_name="demo")
            self.pulid.pipe.set_adapters(["demo"], adapter_weights=[self.demo_scale])

        # The ID encoders (EVA-CLIP, face detector/parser, IDFormer) are only used to
        # compute the per-identity embedding -- keep them on CPU during the diffusion
        # steps so PuLID + demo LoRA fits the 20 GB budget; page them in only to embed.
        fh = getattr(self.pulid, "face_helper", None)
        self._encoders = [
            m for m in (getattr(self.pulid, "clip_vision_model", None),
                        getattr(self.pulid, "id_adapter", None),
                        getattr(fh, "face_det", None), getattr(fh, "face_parse", None))
            if m is not None and hasattr(m, "to")
        ]
        self._encoders_to("cpu")
        gc.collect()
        torch.cuda.empty_cache()
        self._emb: dict = {}

    def identity_phrase(self, gender: str) -> str:
        return gender

    def _encoders_to(self, dev):
        for m in self._encoders:
            try:
                m.to(dev)
            except Exception:  # noqa: BLE001
                pass

    def _embed(self, id_name, ref_path):
        if id_name not in self._emb:
            self._encoders_to(self.device)
            img = self._resize(_read_rgb(ref_path), 1024)
            self._emb[id_name] = self.pulid.get_id_embedding([img])  # (uncond, id)
            self._encoders_to("cpu")
            torch.cuda.empty_cache()
        return self._emb[id_name]

    def generate(self, id_name, gender, ref_path, prompt, seed):
        uncond, idemb = self._embed(id_name, ref_path)
        if self.delay_start > 0:
            self.pulid._delay_uncond = uncond
            self.pulid._delay_start = self.delay_start
            self.pulid._delay_si = 0
        return self.pulid.inference(
            prompt, (1, self.resolution, self.resolution), self.negative,
            idemb, uncond, self.id_scale, self.guidance, self.steps, seed,
        )[0]

    def close(self):
        del self.pulid
        gc.collect()
        torch.cuda.empty_cache()


class IPAdapterMethod(Method):
    """IP-Adapter FaceID Plus v2 (SDXL): insightface ID embedding + CLIP face crop."""

    def __init__(self, mcfg: dict, model_cfg: dict, infer_cfg: dict, device: str):
        _add_path(EXTERNAL / "IP-Adapter")
        from diffusers import StableDiffusionXLPipeline
        from insightface.app import FaceAnalysis
        from insightface.utils import face_align

        from ip_adapter.ip_adapter_faceid import IPAdapterFaceIDPlusXL

        self.device = device
        self.negative = infer_cfg["negative_prompt"]
        self.steps = int(infer_cfg["steps"])
        self.guidance = float(infer_cfg["guidance_scale"])
        self.scale = float(mcfg.get("ip_scale", 1.0))
        self.s_scale = float(mcfg.get("s_scale", 1.0))
        self._align = face_align.norm_crop

        pipe = StableDiffusionXLPipeline.from_pretrained(
            model_cfg["base"], vae=_fp16fix_vae(), torch_dtype=torch.float16,
            use_safetensors=True, add_watermarker=False,
        ).to(device)
        pipe.set_progress_bar_config(disable=True)
        enc = MODELS_DIR / "ip_adapter" / "_hub" / "models" / "image_encoder"
        ckpt = MODELS_DIR / "ip_adapter" / "ip-adapter-faceid-plusv2_sdxl.bin"
        self.ip = IPAdapterFaceIDPlusXL(pipe, str(enc), str(ckpt), device)

        self.app = FaceAnalysis(name="buffalo_l", root=str(INSIGHTFACE_DIR), providers=["CPUExecutionProvider"])
        self.app.prepare(ctx_id=0, det_size=(640, 640))
        self._face: dict = {}

    def identity_phrase(self, gender: str) -> str:
        return gender

    def _face_feats(self, id_name, ref_path):
        if id_name not in self._face:
            import cv2

            img = cv2.imread(str(ref_path))
            faces = self.app.get(img)
            if not faces:  # FFHQ crops sometimes need edge padding to detect
                h, w = img.shape[:2]
                img = cv2.copyMakeBorder(img, h // 4, h // 4, w // 4, w // 4, cv2.BORDER_REPLICATE)
                faces = self.app.get(img)
            emb = torch.from_numpy(faces[0].normed_embedding).unsqueeze(0)
            self._face[id_name] = (emb, self._align(img, landmark=faces[0].kps, image_size=224))
        return self._face[id_name]

    def generate(self, id_name, gender, ref_path, prompt, seed):
        emb, face_img = self._face_feats(id_name, ref_path)
        return self.ip.generate(
            prompt=prompt, negative_prompt=self.negative, face_image=face_img, faceid_embeds=emb,
            shortcut=True, s_scale=self.s_scale, scale=self.scale, num_samples=1,
            num_inference_steps=self.steps, guidance_scale=self.guidance, seed=seed,
        )[0]

    def close(self):
        del self.ip
        gc.collect()
        torch.cuda.empty_cache()


class PhotoMakerMethod(Method):
    """PhotoMaker v2 (SDXL): trigger word 'img' + insightface ID embedding."""

    def __init__(self, mcfg: dict, model_cfg: dict, infer_cfg: dict, device: str):
        _add_path(EXTERNAL / "PhotoMaker")
        from diffusers import EulerDiscreteScheduler

        from photomaker import FaceAnalysis2, PhotoMakerStableDiffusionXLPipeline, analyze_faces

        self.device = device
        self.negative = infer_cfg["negative_prompt"]
        self.steps = int(infer_cfg["steps"])
        self.guidance = float(infer_cfg["guidance_scale"])
        self.start_merge_step = int(mcfg.get("start_merge_step", 10))
        self._analyze = analyze_faces

        self.pipe = PhotoMakerStableDiffusionXLPipeline.from_pretrained(
            model_cfg["base"], vae=_fp16fix_vae(torch.bfloat16), torch_dtype=torch.bfloat16, use_safetensors=True,
        ).to(device)
        ckpt = MODELS_DIR / "photomaker" / "photomaker-v2.bin"
        self.pipe.load_photomaker_adapter(str(ckpt.parent), subfolder="", weight_name=ckpt.name,
                                          trigger_word="img", pm_version="v2")
        self.pipe.fuse_lora()
        self.pipe.scheduler = EulerDiscreteScheduler.from_config(self.pipe.scheduler.config)
        self.pipe.set_progress_bar_config(disable=True)

        self.det = FaceAnalysis2(name="buffalo_l", root=str(INSIGHTFACE_DIR),
                                 allowed_modules=["detection", "recognition"], providers=["CPUExecutionProvider"])
        self.det.prepare(ctx_id=0, det_size=(640, 640))
        self._id: dict = {}

    def identity_phrase(self, gender: str) -> str:
        return f"{gender} img"

    def _id_inputs(self, id_name, ref_path):
        if id_name not in self._id:
            from diffusers.utils import load_image

            pil = load_image(str(ref_path))
            arr = np.array(pil)[:, :, ::-1]  # RGB -> BGR
            faces = self._analyze(self.det, arr)
            if not faces:
                import cv2

                h, w = arr.shape[:2]
                arr = cv2.copyMakeBorder(arr, h // 4, h // 4, w // 4, w // 4, cv2.BORDER_REPLICATE)
                faces = self._analyze(self.det, arr)
            embeds = torch.stack([torch.from_numpy(faces[0]["embedding"])]).unsqueeze(0)
            self._id[id_name] = ([pil], embeds)
        return self._id[id_name]

    def generate(self, id_name, gender, ref_path, prompt, seed):
        id_images, id_embeds = self._id_inputs(id_name, ref_path)
        return self.pipe(
            prompt, negative_prompt=self.negative, input_id_images=id_images, id_embeds=id_embeds,
            num_images_per_prompt=1, start_merge_step=self.start_merge_step,
            num_inference_steps=self.steps, guidance_scale=self.guidance,
            generator=torch.Generator(self.device).manual_seed(seed),
        ).images[0]

    def close(self):
        del self.pipe
        gc.collect()
        torch.cuda.empty_cache()


class HyperLoRAMethod(Method):
    """HyperLoRA zero-shot ID LoRA applied on RealVisXL v4.0 (documented exception
    to the SDXL-1.0 base -- HyperLoRA is incompatible with vanilla SDXL). Consumes
    the per-identity LoRAs pre-generated into models/hyperlora_loras/."""

    TRIGGER = "fcsks fxhks fhyks, "

    def __init__(self, mcfg: dict, model_cfg: dict, infer_cfg: dict, device: str):
        from diffusers import StableDiffusionXLPipeline

        self.device = device
        self.negative = infer_cfg["negative_prompt"]
        self.steps = int(infer_cfg["steps"])
        self.guidance = float(infer_cfg["guidance_scale"])
        self.resolution = int(infer_cfg["resolution"])
        self.lora_scale = float(mcfg.get("lora_scale", 0.8))
        self.clip_skip = int(mcfg.get("clip_skip", 2))
        base = mcfg.get("base", "SG161222/RealVisXL_V4.0")
        try:
            self.pipe = StableDiffusionXLPipeline.from_pretrained(
                base, torch_dtype=torch.float16, use_safetensors=True, variant="fp16"
            ).to(device)
        except Exception:
            self.pipe = StableDiffusionXLPipeline.from_pretrained(
                base, torch_dtype=torch.float16, use_safetensors=True
            ).to(device)
        self.pipe.set_progress_bar_config(disable=True)
        self._loaded: str | None = None

    def identity_phrase(self, gender: str) -> str:
        return gender

    def _ensure_identity(self, id_name: str) -> None:
        if self._loaded == id_name:
            return
        lora = HYPERLORA_LORAS / f"{id_name}.safetensors"
        if not lora.exists():
            raise FileNotFoundError(f"HyperLoRA LoRA missing: {lora} (run scripts/08b_gen_hyperlora_loras.py)")
        self.pipe.unload_lora_weights()
        self.pipe.load_lora_weights(str(lora), adapter_name="hyperid")
        self.pipe.set_adapters(["hyperid"], adapter_weights=[self.lora_scale])
        self._loaded = id_name

    def generate(self, id_name, gender, ref_path, prompt, seed):
        self._ensure_identity(id_name)
        g = torch.Generator(self.device).manual_seed(seed)
        return self.pipe(
            self.TRIGGER + prompt, negative_prompt=self.negative, num_inference_steps=self.steps,
            guidance_scale=self.guidance, height=self.resolution, width=self.resolution,
            clip_skip=self.clip_skip, generator=g,
        ).images[0]

    def close(self):
        del self.pipe
        self._loaded = None
        gc.collect()
        torch.cuda.empty_cache()


_REGISTRY = {
    "dreambooth_lora": DreamBoothMethod,
    "pulid": PuLIDMethod,
    "ip_adapter_faceid": IPAdapterMethod,
    "photomaker": PhotoMakerMethod,
    "hyperlora": HyperLoRAMethod,
}


def build_method(mcfg: dict, model_cfg: dict, infer_cfg: dict, device: str) -> Method:
    kind = mcfg["identity_method"]
    if kind not in _REGISTRY:
        raise ValueError(f"unknown identity_method: {kind!r}")
    return _REGISTRY[kind](mcfg, model_cfg, infer_cfg, device)
